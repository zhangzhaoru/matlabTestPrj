function [Sg11,Sg12,Sg13,Sg21,Sg22,Sg23,Sg31,Sg32,Sg33] = sigmacal(mn1,CV1,mn2,Qo,mn3,Ro,lam);

%---------------------------------------------------------------
Sg11 = mn1;
Xi = lam*sqrt(CV1);
Xin = lam*sqrt(CV1);
Sg12 = mn1 + Xi;
Sg13 = mn1 - Xin;
%---------------------------------------------------------------
Sg21 = mn2;
Xi = lam*sqrt(Qo);
Xin = lam*sqrt(Qo);
Sg22 = mn2 + Xi;
Sg23 = mn2 - Xin;
%---------------------------------------------------------------
Sg31 = mn3;
Xi = lam*sqrt(Ro);
Xin = lam*sqrt(Ro);
Sg32 = mn3 + Xi;
Sg33 = mn3 - Xin;
%---------------------------------------------------------------
Sg1 = [Sg12 Sg13];
Sg2 = [Sg22 Sg23];
Sg3 = [Sg32 Sg33];
%---------------------------------------------------------------
