function [ qout ] = qconj( qin )
%% **************************************************************
%名称：CONJugate Quaternion
%功能：输出共轭四元数
%________________________________________________________________________
% 输入：
%       qin: 输入四元数
% 输出：
%       qout: 输入四运输的共轭
%_________________________________________________________________________
%作者：哈尔滨工程大学 自动化学院 张峥
%日期：2020年10月2日
% ************************************************************************
%% 
qout = zeros(4, 1);

qout(1) = qin(1);
qout(2:4) = -qin(2:4);

end

